# INTEGERS

price = 100

best_price = int(80)

print(type(price))                      #   <class 'int'>

print(isinstance(best_price, int))      #   True


# FLOAT

gpa = 3.28

y = float(1.14)

print(type(gpa))                      #   <class 'float'>

print(isinstance(y, float))           #   True


# COMPLEX

x = 3+5j

print(type(x))                         #    <class 'complex'>

print(x.real)                          #    3.0

print(x.imag)                          #    5.0